#include <unistd.h>

void	aff_case(int grille[4][4])
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
			write(1, &grille[i][j++], 1);
		write(1, "\n", 1);
		i++;
	}
}
